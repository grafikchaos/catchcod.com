        <blockquote class='excerpt'>
            <p>
                January 1949: A blustery cold winter's night on the north shore of Round Lake, seven miles from the nearest community of Nisswa, Minnesota.  Bare tree limbs, a landscape covered by a foot of snow and a  temperature of 6 degrees F. attest to the reality that this is the dead of winter. Much earlier what sun  had existed had dipped into the far southern horizon. Most residents have long ago migrated south with the only reminder of current habitation being an occasional light flickering in the far-off distance.
            </p>

            <p>
                From the outside it is impossible to know just what is taking place within any one of the currently- inhabited homes. In one of these homes, in the tuck-under garage of the home on the north shore of frozen Round Lake, a high school basketball game is about to take place. How can this be possible?  Upon entering that garage, one realizes that this is, in fact, true. A game is about to take place between New York High School (must be a really large school ) and B B Balone (as in sausage) High School. Yes, Hormel hot dogs are being sold at the concession stand.
            </p>

            <p>
                The excitement level is high; however, playing conditions are quite precarious. The game time temperature is 48 degrees, thanks to the presence of one portable electric heater. A puddle of half-thawed slush deposited by the car usually parked in this space covers a good portion of the concrete playing surface and a single 60-watt bulb provides some light and a lot of shadows. The floor itself is also very unique. There is a single basket approximately six feet from the floor and the total length of the floor is eighteen feet.  Along the side of the floor sit the usual items found in a garage: rakes, shovels, a lawn mower and a variety of paint cans. Fortunately ,no fire inspectors have shown up.
            </p>

            <p>
                How is it that this game is about to take place in this setting? Only one answer: Jimmie Storm has a very rich imagination.
            </p>

            <p>
                Time to check in on the action. The pep bands are playing the school fight songs. New York High School shares the same song with Brainerd, Minnesota's high school. The cheerleaders have succeeded in raising the level of excitement to a fever pitch and the radio announcer (no television yet at this location), after a brief commercial for Bob's Auto Mart in Brainerd, has adjusted his earphones and is ready to provide the play-by-play account of the game over the local airways. B B Balone players in their traditional orange and black uniforms are on the floor warming up. Now coming on the floor in their blue and white uniforms are the lads from New York High School.
            </p>

            <p>
                Time for player introductions. New York High School: center: John Leonard. He and Jimmie Storm look like identical twins. However, the same can be said about all others assembled as well. Guards:   Squash Leonard, John's younger brother, and Dick Kazmaier. The same Dick Kazmaier who was a football star at Princeton University? If so, has anyone checked his eligibility? (Due to his very recent death, we will never know the answer to this one.)  Two forwards round out the starting five: Dick Haystack and Dick Pigeon. This was before the day of point guards.
            </p>

            <p>
                Due to multitasking overload, the starting five from B B Balone are not individually introduced.
            </p>

            <p>
                The game itself is a fierce battle between these two rivals. However, in the end it is New York High School that pulls out the victory. One who follows this team understands that this is almost always the case.
            </p>

            <p>
                Details of the game will be written the following day by the sportswriter. Yes, you guessed it. The sportswriter will be Jimmie Storm sitting at his desk at Nisswa Elementary School. It will also be noted that the next home game for New York High School will be against the purple and white of Mediterranean High School. Must be a really long road trip for them.
            </p>
