            <h2>II. Application Process</h2>
            <p><strong>A. The Foundation takes into account a number of criteria</strong> when making funding decisions. Among the primary criteria are the following:</p>
      
            <ul class='bodyBullets'>
                <li>Over-all operating strength of the organization, including its board and demonstrated executive leadership and level of board participation.</li>
                <li>Need for and demonstrated effectiveness of its programs.</li>
                <li>Geographic area in which services are delivered &ndash; focus on Minnesota.</li>
                <li>Adherence to application guidelines established by the Foundation.</li>
                <li>Quality of application materials.</li>
                <li>Areas of Service provided (See Background Information).</li>
                <li>Completion or planned completion of an organizational assessment process, such as the Accountability Wizard offered by the Minnesota Charities Review Council.</li>
            </ul>


            <p><strong>B. Each request for funding requires a Letter of Inquiry</strong> &mdash; no longer than two pages &mdash; in which the applicant organization addresses the following points:</p>
            <ul class="bodyBullets">
                <li>A statement that the organization is currently a tax-exempt organization (The Foundation does not fund individuals.).</li>
                <li>Amount of proposed request.</li>
                <li>Brief description of the focus of the funding request and how this request meets Foundation guidelines.</li>
                <li>Brief description of current organizational leadership.</li>
                <li>Description of the primary roles played by the governing board.</li>
                <li>A figure indicating average percent of board members in attendance at board meetings over the past 12-month period of time.</li>
                <li>Indication whether the organization has completed or plans to complete an organizational self-assessment and the year it was completed or is planned to be completed. (See Little Red Bus &mdash; The Check Up &mdash; for description of an organizational assessment.)</li>
                <li>Include contact name, email and phone number.</li>
            </ul>

