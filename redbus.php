            <h2>Listening to New Voices</h2>
            <p>It was the beginning of another year of exciting journeys on the Little Red Bus. In addition to the 15 passengers who had been on trips over the past several years, there were four new passengers for whom this was a first time experience.</p>
            <p>For the returning passengers the first trip of the new year was a pleasant return to &quot;business as usual.&quot; However, the new passengers, while generally enjoying the trip, discovered ways in which the trip might be even better. When they shared their thoughts with the veterans, they quickly learned that there was very little interest in making any changes among the old timers.</p>
            <p>As a result, some potentially valuable insights offered by the new comers never received attention. After a while the new members of the group either adjusted to the &quot;business as usual&quot; routine or left the outings altogether feeling frustrated that their suggestions had not been taken seriously.</p>
            <p>How does this translate into the experiences of a governing board? While it is important to understand and respect the history of an organization, the board also plays a very critical role in helping the organization grow and change. &quot;Business as usual&quot; leads to organizational stagnation.</p>
            <h3>Questions for consideration:</h3>
            <ul class="bodyBullets">
            	<li>Is the organization really committed to continued growth and change?</li>
            	<li>Are long-term board members open to listening to the observations of new board members?</li>
            	<li>Are new board members viewed as consultants and encouraged to express their views on a regular basis?</li>
            	<li>Are new board members viewed other than &quot;I am just a new board member learning how things are done here&quot;?</li>
            </ul>
            <p>An excellent way to engage new board members is to view them as your internal consultants. For the first year on the board they will see things that veteran board members no longer see. By encouraging new board members to offer their fresh and candid insights, the organization will grow and new board members will feel engaged with the organization and have a sense of ownership.</p>
